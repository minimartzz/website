
$('.njc-ssbut').click(function() {
    $('.njc-img').animate({opacity: 1}, 'slow');
    $('.njc-slideshow').animate({opacity: 0}, 'slow');
    $('.njc-carousel-hidden').css('visibility', 'visible');
    setTimeout(function() {$('.njc-ssbut').css({visibility: 'hidden'})}, 500);
    setTimeout(function() {$('.njc-ssbut2').css({visibility: 'visible'})}, 500);
});

